#!/bin/bash

# ==========================================================
# Lost Book Robot - 模型多场景验证脚本
# ==========================================================

# 【关键修复】将当前目录添加到 PYTHONPATH，解决导入错误
export PYTHONPATH=$PYTHONPATH:$(pwd)

# 配置路径（请根据你的实际环境调整）
CHECKPOINT="logic/outputs/run_20260402_164525/best_comparator.pth"
INFERENCE_SCRIPT="modules/logic/inference.py"

echo "开始验证 Lost Book Robot 模型性能..."
echo "使用权重文件: $CHECKPOINT"

# 定义测试函数
run_test() {
    local category=$1
    local text_a=$2
    local text_b=$3
    echo -e "\n>>> [测试类别]: $category"
    python "$INFERENCE_SCRIPT" --text-a "$text_a" --text-b "$text_b" --checkpoint "$CHECKPOINT"
}

# ----------------------------------------------------------
# 1. 基础排序验证 (In_Order)
# ----------------------------------------------------------
# 测试主类数字递增 
run_test "基础分类号排序" "BR 330 E5 1955" "BR 350 E5 1955"

# 测试卷册顺序 
run_test "卷册/后缀排序" "BR 330 E5 1955 v.10" "BR 330 E5 1955 v.11"

# ----------------------------------------------------------
# 2. 重复与前缀抗噪 (Duplicate)
# ----------------------------------------------------------
# 测试相同索书号，忽略特殊前缀 
run_test "带馆藏前缀的重复" "OLIN BV 4208 .18 L48 2001" "BV 4208 18 L48 2001"

# 测试带超大本标识 (++) 的完全一致 
run_test "完全一致的副本" "CD 1040 A25++" "CD 1040 A25++"

# ----------------------------------------------------------
# 3. 错误顺序检测 (Out_of_Order)
# ----------------------------------------------------------
# 测试主类字母顺序错误
run_test "主类字母错位" "DS 135 E83 R37" "DA 880 A7 M12"

# 测试 Cutter 字母错位 
run_test "Cutter字母错位" "DS 247 .K85 M95" "DS 247 .K85 M21"

# ----------------------------------------------------------
# 4. OCR 修复能力验证 (预处理鲁棒性)
# ----------------------------------------------------------
# 验证 preprocess_lcc 是否成功修复断裂字母 "B R" -> "BR" 
run_test "OCR 字母断裂修复" "B R 330 E5 1955" "BR 350 E5 1955"

# 验证对混淆字符 '0' vs 'O' 的容忍度
run_test "OCR 字符混淆容忍" "OLIN BR 67 .P38 2006" "OLIN BR 67 .P38 201O"

# ----------------------------------------------------------
# 5. LCC 硬核逻辑 (Cutter 小数点比较)
# ----------------------------------------------------------
# 根据 data_gen.py 逻辑：.C645 (Cutter部分视为 0.645) < .C65 (视为 0.65) 
run_test "Cutter 小数深度逻辑" "QA 76 .C645 2023" "QA 76 .C65 2023"

# ----------------------------------------------------------
# 6. 年份与版本后缀
# ----------------------------------------------------------
# 验证年份后的 a/b/z 后缀处理 
run_test "年份版本排序" "PA 6637 .Z8x 1857" "PA 6637 .Z8x 1857a"

echo -e "\n=========================================================="
echo "测试流程结束。"